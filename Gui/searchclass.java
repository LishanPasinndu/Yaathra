    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() == 2) {

            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please Select a Class", "warning", JOptionPane.WARNING_MESSAGE);
            } else {

                String classno = jTable1.getValueAt(r, 0).toString();
                String time = jTable1.getValueAt(r, 1).toString();
                String subject = jTable1.getValueAt(r, 3).toString();
                String teacher = jTable1.getValueAt(r, 6).toString();

                if (css != null) {

                    css.jLabel7.setText(classno);
                    css.jLabel6.setText(time);
                    css.jLabel8.setText(teacher);
                    css.jLabel9.setText(subject);

                    css.jButton8.setEnabled(false);

                    this.dispose();
                }

                if (ma != null) {

                    ma.jLabel20.setText(classno);
                    ma.jLabel21.setText(teacher);
                    ma.jLabel22.setText(time);
                    ma.jLabel23.setText(subject);

                    ma.jButton3.setEnabled(false);

                    this.dispose();

                }

            }
        }


    }//GEN-LAST:event_jTable1MouseClicked

