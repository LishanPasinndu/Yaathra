    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String class_id = cla.getText();
        String gradee = gra.getText();
        String teacherr = tea.getText();
        String subject = sub.getText();
        String feee = fe.getText();
        String barcode = bar.getText();
        String name = na.getText();
        String month = jComboBox2.getSelectedItem().toString();

        if (class_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select a Class", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (barcode.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select a Student", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (month.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select a Month", "warning", JOptionPane.WARNING_MESSAGE);
        } else {

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            boolean isFound = false;
            int x = -1;

            for (int i = 0; i < dtm.getRowCount(); i++) {
                String b = jTable1.getValueAt(i, 4).toString();
                String c = jTable1.getValueAt(i, 0).toString();
                String m = jTable1.getValueAt(i, 6).toString();

                if (b.equals(barcode) && c.equals(class_id) && m.equals(month)) {
                    isFound = true;
                    x = i;
                    break;
                }
            }

            if (isFound) {

                JOptionPane.showMessageDialog(this, "This Payment Already added", "Warning", JOptionPane.WARNING_MESSAGE);
                resetFields();
                jButton8.setEnabled(true);
                jButton9.setEnabled(true);

            } else {

                Vector v = new Vector();
                v.add(class_id);
                v.add(teacherr);
                v.add(subject);
                v.add(gradee);
                v.add(barcode);
                v.add(name);
                v.add(month);
                v.add(feee);

                dtm.addRow(v);

                updateTotal();
                resetFields();
                jButton8.setEnabled(true);
                jButton9.setEnabled(true);

                JOptionPane.showMessageDialog(this, "Student Added to the table", "Success", JOptionPane.INFORMATION_MESSAGE);

            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

