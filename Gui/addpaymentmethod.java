    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String m = jTextField1.getText();

        if (m.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter a Payment Method Name", "warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs = MySQL.search("SELECT * FROM `payement_type` WHERE `name`='" + m + "' ");

                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This Method is already added!", "warning", JOptionPane.WARNING_MESSAGE);
                    jTextField1.setText("");
                } else {

                    MySQL.iud("INSERT INTO `payement_type`(`name`) VALUES('" + m + "') ;");

                    loadmethod();
                    jTextField1.setText("");

                    JOptionPane.showMessageDialog(this, "Payment Method Added!", "Success", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

