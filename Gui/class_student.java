    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String class_id = jLabel7.getText();
        String time = jLabel6.getText();
        String teacher = jLabel8.getText();
        String subject = jLabel9.getText();

        String stu_id = jLabel11.getText();
        String name = jLabel12.getText();
        String contact = jLabel13.getText();

        if (class_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select a Class", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (stu_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select a Student", "warning", JOptionPane.WARNING_MESSAGE);
        } else {

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            Vector v = new Vector();
            v.add(class_id);
            v.add(time);
            v.add(teacher);
            v.add(subject);
            v.add(stu_id);
            v.add(name);
            v.add(contact);

            dtm.addRow(v);

            resetFields();
            jButton8.setEnabled(true);
            jButton9.setEnabled(true);

        }


    }//GEN-LAST:event_jButton1ActionPerformed

