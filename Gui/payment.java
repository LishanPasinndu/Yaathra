    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:

        if (jTable1.getRowCount() <= 0) {

            JOptionPane.showMessageDialog(this, "Please added payment in table", "warning", JOptionPane.WARNING_MESSAGE);

        } else {

            DateTimeFormatter dttf = DateTimeFormatter.ofPattern("yyyy/MM/dd");
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("HH:mm:ss");
            LocalDateTime now = LocalDateTime.now();
            String date = dttf.format(now);
            String time = dtf.format(now);

            for (int i = 0; i < jTable1.getRowCount(); i++) {

                String payment_method = jComboBox1.getSelectedItem().toString();
                String payment = jTextField3.getText();
                double d = Double.parseDouble(payment);
                String paymentt = df.format(d);
                String balance = jLabel26.getText();
                double f = Double.parseDouble(balance);
                String balancee = df.format(f);

                String fee = jTable1.getValueAt(i, 7).toString();
                double g = Double.parseDouble(fee);
                String feee = df.format(g);
                String name = jTable1.getValueAt(i, 5).toString();
                String grade = jTable1.getValueAt(i, 3).toString();
                String subject = jTable1.getValueAt(i, 2).toString();
                String teacher = jTable1.getValueAt(i, 1).toString();
                String cno = jTable1.getValueAt(i, 0).toString();
                String bar = jTable1.getValueAt(i, 4).toString();
                String month = jTable1.getValueAt(i, 6).toString();

                try {

                    ResultSet rs2 = MySQL.search("SELECT `Sno` FROM `student` WHERE `barcode`='" + bar + "'  ");
                    rs2.next();
                    String Sno = rs2.getString("Sno");

                    ResultSet rs3 = MySQL.search("SELECT `id` FROM `payement_type` WHERE `name`='" + payment_method + "'  ");
                    rs3.next();
                    String pid = rs3.getString("id");

                    ResultSet rs1 = MySQL.search("SELECT * FROM `invoice` WHERE `month`='" + month + "' AND `student_Sno`='" + Sno + "' AND `ClassNo`='" + cno + "' AND `payment`='" + payment + "' AND `balance`='" + balance + "' ");

                    if (rs1.next()) {

                        JOptionPane.showMessageDialog(this, "This Payment already added", "warning", JOptionPane.WARNING_MESSAGE);

                    } else {

                        MySQL.iud("INSERT INTO `invoice`(`month`,`student_Sno`,`ClassNo`,`payement_type_id`,`payment`,`balance`) "
                                + "VALUES('" + month + "','" + Sno + "','" + cno + "','" + pid + "','" + payment + "','" + balance + "') ");

                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }

                try {
                    JOptionPane.showMessageDialog(this, "Payment Success!", "Saved", JOptionPane.INFORMATION_MESSAGE);

                    //report
                    JasperReport jr = JasperCompileManager.compileReport(getClass().getResourceAsStream("/reports/yathrainvoice.jrxml"));

                    HashMap<String, Object> data = new HashMap<String, Object>();
                    data.put("cno", cno);
                    data.put("tea", teacher);
                    data.put("sub", subject);
                    data.put("gra", grade);
                    data.put("bar", bar);
                    data.put("name", name);
                    data.put("month", month);
                    data.put("fee", feee);
                    data.put("pay", paymentt);
                    data.put("bala", balancee);
                    data.put("date", date);
                    data.put("time", time);

                    //TableModel model = jTable1.getModel();
                    //JRTableModelDataSource dataSource = new JRTableModelDataSource(model);
                    JREmptyDataSource dataSource = new JREmptyDataSource();
                    JasperPrint jp = JasperFillManager.fillReport(jr, data, dataSource);

                    JasperViewer.viewReport(jp,false);
                    //report

                } catch (Exception e) {
                    e.printStackTrace();
                }

            }

            resetFields();
            jLabel22.setText("0.00");
            jComboBox1.setSelectedItem("Select");
            jTextField3.setText(" ");
            jLabel26.setText("0.00");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
        }


    }//GEN-LAST:event_jButton5ActionPerformed

