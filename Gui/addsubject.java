    public void loadSubject() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `subject` INNER JOIN `medium` ON `medium`.`id`=`subject`.`medium_id` WHERE `status`='1'  ORDER BY `subNo` ASC ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("subNo"));
                v.add(rs.getString("name"));
                v.add(rs.getString("medium.name"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
        }

    }

    public void loadmedium() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `medium`;");

            Vector v = new Vector();
            v.add("Select");

            while (rs.next()) {
                v.add(rs.getString("name"));
            }

            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

