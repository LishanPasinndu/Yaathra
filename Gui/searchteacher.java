    public void loadteacher() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `teacher` INNER JOIN `city` ON `city`.`id`=`teacher`.`city_id` WHERE `status`='1' ORDER BY `Tno` ASC ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("Tno"));
                v.add(rs.getString("fname"));
                v.add(rs.getString("lname"));
                v.add(rs.getString("contact"));
                v.add(rs.getString("line1"));
                v.add(rs.getString("line2"));
                v.add(rs.getString("city.name"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
        }

    }

    public void searchStudent() {

        try {

            String Sno = jTextField1.getText();
            String name = jTextField2.getText();

            Vector queryVector = new Vector();

            //Tno
            if (Sno.isEmpty()) {
                loadteacher();
            } else {
                queryVector.add("`teacher`.`Tno`= '" + Sno + "'  ");
            }
            //Tno

            //name
            if (name.isEmpty()) {
                loadteacher();
            } else {
                queryVector.add("`teacher`.`fname` LIKE '%" + name + "%'  ");
            }
            //name

            String wherequery = "WHERE";

            for (int i = 0; i < queryVector.size(); i++) {
                wherequery += " ";
                wherequery += queryVector.get(i);
                wherequery += " ";
                if (i != queryVector.size() - 1) {
                    wherequery += "AND";
                }
            }

            ResultSet rs = MySQL.search("SELECT * FROM `teacher` INNER JOIN `city` ON `city`.`id`=`teacher`.`city_id` " + wherequery + " ; ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("Tno"));
                v.add(rs.getString("fname"));
                v.add(rs.getString("lname"));
                v.add(rs.getString("contact"));
                v.add(rs.getString("line1"));
                v.add(rs.getString("line2"));
                v.add(rs.getString("city.name"));

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

