    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String name = jTextField1.getText();
        String medium = jComboBox1.getSelectedItem().toString();

        int m = jComboBox1.getItemCount();

        if (m <= 1) {

            JOptionPane.showMessageDialog(this, "Go to admin features & Add Medium !", "warning", JOptionPane.WARNING_MESSAGE);
            this.dispose();
            addmedium am = new addmedium();
            am.setVisible(true);

        } else if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Subject Name", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (medium.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please enter Subject Medium", "warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs1 = MySQL.search("SELECT `id` FROM `medium` WHERE `name`='" + medium + "'");
                rs1.next();

                String m_id = rs1.getString("id");

                ResultSet rs = MySQL.search("SELECT * FROM `subject` WHERE `name`='" + name + "' AND `medium_id`='" + m_id + "' AND `status`='1' ");

                if (rs.next()) {

                    JOptionPane.showMessageDialog(this, "This Suubject Already Added", "warning", JOptionPane.WARNING_MESSAGE);
                    jTextField1.setText("");

                } else {

                    MySQL.iud("INSERT INTO `subject`(`name`,`status`,`medium_id`) VALUES('" + name + "','1','" + m_id + "') ");

                    loadSubject();
                    jTextField1.setText("");
                    jComboBox1.setSelectedItem("Select");

                    JOptionPane.showMessageDialog(this, "Subject Added Successfull", "Success!", JOptionPane.INFORMATION_MESSAGE);

                    at.loadsubject();
                    ac.loadSubject();

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

