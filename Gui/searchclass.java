    public void loadClass() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `class` INNER JOIN `subject` ON `subject`.`subNo`=`class`.`subject_subNo` INNER JOIN `teacher` ON `teacher`.`Tno`=`class`.`teacher_Tno` INNER JOIN `medium` ON `medium`.`id`=`class`.`medium_id` WHERE `class`.`status`='1'  ORDER BY `ClassNo` ASC ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("ClassNo"));
                v.add(rs.getString("time_to"));
                v.add(rs.getString("time_from"));
                v.add(rs.getString("subject.name"));
                v.add(rs.getString("class.grade"));
                v.add(rs.getString("medium.name"));
                v.add(rs.getString("teacher.fname") + " " + rs.getString("teacher.lname"));
                v.add(rs.getString("class.fee"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
        }

    }

    public void searchClass() {

        try {

            String class_no = jTextField1.getText();
            String teacher = jTextField2.getText();
            String subject = jTextField3.getText();
            String medium = jTextField4.getText();
            String grade = jTextField5.getText();

            Vector queryVector = new Vector();

            //class_no
            if (class_no.isEmpty()) {
                loadClass();
            } else {
                queryVector.add("`class`.`ClassNo`= '" + class_no + "'  ");
            }
            //class_no

            //teacher
            if (teacher.isEmpty()) {
                loadClass();
            } else {
                queryVector.add("`teacher`.`fname` LIKE '%" + teacher + "%'  ");
            }
            //teacher

            //subject
            if (subject.isEmpty()) {
                loadClass();
            } else {
                queryVector.add("`subject`.`name` LIKE '%" + subject + "%'  ");
            }
            //subject

            //subject
            if (medium.isEmpty()) {
                loadClass();
            } else {
                queryVector.add("`medium`.`name` LIKE '%" + medium + "%'  ");
            }
            //subject

            //grade
            if (grade.isEmpty()) {
                loadClass();
            } else {
                queryVector.add("`class`.`grade` = '" + grade + "'  ");
            }
            //grade

            String wherequery = "WHERE";

            for (int i = 0; i < queryVector.size(); i++) {
                wherequery += " ";
                wherequery += queryVector.get(i);
                wherequery += " ";
                if (i != queryVector.size() - 1) {
                    wherequery += "AND";
                }
            }

            ResultSet rs = MySQL.search("SELECT * FROM `class` INNER JOIN `teacher` ON `teacher`.`Tno`=`class`.`teacher_Tno` INNER JOIN `subject` ON `subject`.`subNo`=`class`.`subject_subNo` INNER JOIN `medium` ON `medium`.`id`=`class`.`medium_id`" + wherequery + " ; ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("ClassNo"));
                v.add(rs.getString("time_to"));
                v.add(rs.getString("time_from"));
                v.add(rs.getString("subject.name"));
                v.add(rs.getString("class.grade"));
                v.add(rs.getString("medium.name"));
                v.add(rs.getString("teacher.fname") + " " + rs.getString("teacher.lname"));
                v.add(rs.getString("class.fee"));

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

