    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:

        if (jTable1.getRowCount() <= 0) {

            JOptionPane.showMessageDialog(this, "Please added student & class", "warning", JOptionPane.WARNING_MESSAGE);

        } else {

            for (int i = 0; i < jTable1.getRowCount(); i++) {

                String cid = jTable1.getValueAt(i, 0).toString();
                String sid = jTable1.getValueAt(i, 4).toString();

                try {
                    ResultSet rs1 = MySQL.search("SELECT * FROM `class_student` WHERE `class_id`='" + cid + "' AND `student_id`='" + sid + "' ");

                    if (rs1.next()) {

                        JOptionPane.showMessageDialog(this, "This student already added This Class", "warning", JOptionPane.WARNING_MESSAGE);

                    } else {

                        MySQL.iud("INSERT INTO `class_student`(`class_id`,`student_id`) VALUES('" + cid + "','" + sid + "') ");

                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }

            }
            JOptionPane.showMessageDialog(this, "Student Added Class!", "Saved", JOptionPane.INFORMATION_MESSAGE);

            resetFields();

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
        }


    }//GEN-LAST:event_jButton5ActionPerformed

